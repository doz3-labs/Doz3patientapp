// ── Helper functions ─────────────────────────────────────
export function getProductsByCategory(category: string): Product[] {
  if (category === 'all') return ALL_PRODUCTS;
  return ALL_PRODUCTS.filter(p => p.category === category);
}

export function getProductById(id: string): Product | undefined {
  return ALL_PRODUCTS.find(p => p.id === id);
}

export function searchProducts(query: string, category?: string): Product[] {
  const base = category && category !== 'all'
    ? ALL_PRODUCTS.filter(p => p.category === category)
    : ALL_PRODUCTS;
  if (!query.trim()) return base;
  const q = query.toLowerCase();
  return base.filter(p =>
    p.name.toLowerCase().includes(q) ||
    p.brand.toLowerCase().includes(q) ||
    p.subcategory.toLowerCase().includes(q) ||
    p.description.toLowerCase().includes(q)
  );
}

export function getFeaturedProducts(count = 6): Product[] {
  return ALL_PRODUCTS.filter(p => p.discount).slice(0, count);
}

export function getTopRated(count = 6): Product[] {
  return [...ALL_PRODUCTS].sort((a, b) => b.rating - a.rating).slice(0, count);
}
